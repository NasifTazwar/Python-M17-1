animal = "Dolphin"
years = 10
is_learner = False
mass = 45.7

# Printing Different Variables and their Data Type
print("Animal :", animal)
print("Data Type of Animal is", type(animal))

print("Years :", years)
print("Data Type of Years is", type(years))

print("is_learner :", is_learner)
print("Data Type of is_learner is", type(is_learner))

print("Mass :", mass)
print("Data Type of Mass is", type(mass))

# Type casting to convert the datatype of variables
print("\n After Type Casting .... ")
years = str(years)
print(years)
print("Data Type of Years is", type(years))
mass = int(mass)
print(mass)
print("Data Type of Mass is", type(mass))

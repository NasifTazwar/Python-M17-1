# Taking input from the user
plant1 = int(input("Enter the value for plant1: "))
plant2 = int(input("Enter the value for plant2: "))
plant3 = int(input("Enter the value for plant3: "))
plant4 = int(input("Enter the value for plant4: "))
plant5 = int(input("Enter the value for plant5: "))

# Finding the total of plants
total = plant1 + plant2 + plant3 + plant4 + plant5
print("The total of all the 5 plants is: ", total)

# Finding the average of plants
average = total / 5
print("The average of all the plants is: ", average)
